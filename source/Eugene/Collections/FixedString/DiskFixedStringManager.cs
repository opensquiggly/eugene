namespace Eugene.Collections;

public class DiskFixedStringManager
{
  // /////////////////////////////////////////////////////////////////////////////////////////////
  // Constructors
  // /////////////////////////////////////////////////////////////////////////////////////////////

  public DiskFixedStringManager(IDiskBlockManager diskBlockManager, int arrayBlockTypeIndex)
  {
    DiskBlockManager = diskBlockManager;
    ArrayBlockTypeIndex = arrayBlockTypeIndex;
  }

  // /////////////////////////////////////////////////////////////////////////////////////////////
  // Public Properties
  // /////////////////////////////////////////////////////////////////////////////////////////////

  public IDiskBlockManager DiskBlockManager { get; }

  public int ArrayBlockTypeIndex { get; }

  // /////////////////////////////////////////////////////////////////////////////////////////////
  // Public Methods
  // /////////////////////////////////////////////////////////////////////////////////////////////

  public DiskFixedStringFactory CreateFactory(short dataBlockTypeIndex)
  {
    return new DiskFixedStringFactory(this, dataBlockTypeIndex);
  }
}
